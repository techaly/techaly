                <h2>
            <font color="red">Nikt nie jest chroniony przed nadużyciami medycznymi. Jak lekarze wysyłają ludzi na tamten świat dla pieniędzy.
 </font>
        </h2>
                <br>
                <p><b>Korespondent: Agato, dlaczego nigdy nie opowiadałaś o swojej poważnej chorobie? Ostatnio znikłaś z przestrzeni wiadomości i praktycznie żyłaś w izolacji, nie kontaktowałaś się z nikim.
 </b></p>

                <p><b>Agata Młynarska:</b> Uważam, że to moja sprawa i nie należy obciążać innych swoimi problemami. Poza tym myślałam, że problem nie był na tyle poważny, że bym naprawdę się martwiła. Dlatego zdecydowałam, że nie będę tego publicznie opowiadała.
                </p>
                <br>
                <p><b>Korespondent: "Ale teraz zdecydowałaś się nią podzielić?"</b></p>

                <p><b>Agata Młynarska: </b> Teraz zdecydowałam się. Ale to nie tyle chęć opowiedzenia o swojej chorobie, co raczej chęć pomocy ludziom, którzy tak jak ja są zjadani od wewnątrz przez pasożyty, przez które w każdej chwili można umrzeć. Nikt z nas nie jest odporny na komplikacje medyczne i dzieje się to bardzo nagle. Dziś jesteś zdrową osobą, a jutro praktycznie trupem, który nie ma szans się wydostać z tego. Większość lekarzy po prostu wyciągnie od ciebie pieniądze, póki jest możliwość. I będzie spokojnie patrzeć, jak umierasz.</p>
                <p><strong>Korespondent: "Co masz na myśli?"</strong></p>
                <p><b>Agata Młynarska: </b> Dokładnie to, co powiedziałam. Kiedy ukazały się pierwsze objawy zarażenia pasożytami, nawet nie rozumiałam, co się stało. Nie wszyscy wiedzą, że jeśli na skórze pojawiają się brodawki, szybko się męczysz, az ust wydobywa się bardzo nieprzyjemny zapach, to oznacza, że ​​w organizmie namnażają się pasożyty. W końcu poszłam do lekarza i postawił właściwą diagnozę. Co prawda, udało im się to dopiero za drugim razem, bo za pierwszym razem przepisali mi kilka środków uspokajających, trochę kremów na brodawczaka i inne bzdury. Za drugim razem zbadali mnie dokładniej, przeprowadzili wiele testów i stwierdzili, że w środku żyją co najmniej trzy rodzaje pasożytów, które aktywnie się rozmnażają. Byłam w szoku i zwróciłam się do specjalisty, którego nie było łatwo znaleźć - parazytologa. Powiedział, żeby nie brać już wcześniejszych leków, przepisał wiele nowych i powiedział, że powinny pomóc. Dopiero później zdałam sobie sprawę, po obliczeniu kwoty, którą można ze mnie wyciągnąć, lekarze chcieli tylko, aby choroba zaszła dalej.</p>
                <p>Przez około dwa miesiące wszystko było w porządku. Tak, czułam się bardzo zmęczona, nadal pojawiały się nowe brodawczaki, okresowo coś bolało, ale nic krytycznego dla zdrowia. Wtedy nagle pojawił się ostry ból w okolicy nerek. Ni stąd ni z owąd. Wieczorem, gdy położyłam się spać, wszystko było w porządku. A rano ledwo wstałam z łóżka, ból był tak silny. Natychmiast poszłam do lekarza.</p>
                <p><b>Korespondent: "I co ci powiedział?"</b></p>
                <p><b>Agata Młynarska: </b> Zawstydzony, spanikowany. Powiedzieł, że to larwy pasożytów gryzące żywcem moją prawą nerkę. Postawili przed wyborem, albo kłade się pod nóż i oczyszczają wszystko drogą operacyjną lub przechodzę konserwatywne leczenie w szpitalu. W przeciwnym razie nerka po prostu przestanie pracować, a larwy rozprzestrzenią się po całym ciele i zaczną niszczyć inne narządy. To doprowadzi gwarantowanie do śmieci. Taka operacja w moim wieku to bardzo niebezpieczny krok, więc jedyną alternatywą był szpital. Nie powiem, jaką cenę podali, ale przekroczyła wszelkie rozsądne koszty. I oczywiście wszystko przeszło poza kasę. Oficjalnie zabiegi przeprowadzono za grosze. Teraz rozumiem, że to wszystko było oszustwem w celu osiągnięcia zysku i zrobili to kosztem mojego zdrowia. Ale jeśli istnieje realne zagrożenie śmiercią, logika nie działa.</p>
                <p>Zgodziłam się z podaną kwotą, spędziłam trzy tygodnie w szpitalu na leczeniu stacjonarnym, a potem przychodziłam tam codziennie. Podczas leczenia ból najpierw zmniejszył się, a następnie całkowicie zniknął.
                </p>
                <p><strong>Korespondent: "Więc to pomogło?"</strong></p>
                <p><b>Agata Młynarska:</b> Jeśli nie myśleć o tym, że to wszystko można było o wiele łatwiej zatrzymać, to tak, pomogło. Ale z ich punktu widzenia po prostu zarobili pieniądze na kolejnym pacjencie. Ponadto pomoc była tak lokalna, że ​​z powodzeniem wpłynęła na tylko jeden typ pasożyta. W sumie były trzy typy, czyli, co najmniej trzy gniazda tych potworów. Na dalszym etapie sytuacja rozwinęła się bardzo źle. Zaczęły się bóle w sercu, ciśnienie mocno podskoczyło, chociaż nigdy wcześniej to się nie zdarzało, głowa bardzo mnie bolała, nogi zaczęły puchnąć. Czasami nie mogłam wstać z łóżka przez cały dzień.</p>
                <p>Wciąż regularnie odwiedzałam lekarzy, wydawałam dużo pieniędzy na drogie leki, zażywałam góry tabletek. I każdego dnia coraz bardziej rozpadałam się na kawałki. Szczerze mówiąc, w pewnym momencie pomyślałam, że tak i umrę. Ani przez chwilę nie czułam poprawy.
                </p>
                <p><strong>Korespondent: "Jednak siedzisz tu przede mną. Jak sobie z tym poradziłaś?" </strong></p>
                <p><b>Agata Młynarska:</b> Pomógł przypadek. Z wizytą przyszedł stary znajomy, który obecnie pracuje w Ministerstwie Zdrowia. Widział mój stan, zaczął pytać. Byłoby głupotą zaprzeczać wszystkiemu, opowiedziałam mu wszystko tak, jak jest. I uratował mnie. Co prawda, na początku bardzo się rozgniewał i zaczął wypytywać o nazwiska moich lekarzy. Później wykonał kilka telefonów i tego samego dnia przynieśli mi nowy preparat na pasożyty, który został opracowany w NCHO. Prawie odmówiłam wtedy, bo on nawet nie wziął ode mnie pieniędzy i preparat widziałam po raz pierwszy. Ale z drugiej strony nie było nic do stracenia, więc postanowiłam spróbować.</p>
                <p>Już następnego ranka wstałam z łóżka po raz pierwszy 15 minut po przebudzeniu, a nie po 3-4 godzinach. A po 12-14 dniach zdałam sobie sprawę, że ból zaczął ustępować. Stopniowo zanikały również objawy powikłania. Jak wyjaśniali mi lekarze, których polecił mi znajomy, środek zniszczył pasożyty i zatrzymał ich zdolność do rozmnażania się. Nie masz pojęcia, co ze mnie wyszło w pierwszych dniach po rozpoczęciu przyjmowania preparatu. To był horror. Zresztą po 3 tygodniach znów poczułam się zdrowa. Biorąc pod uwagę, jak ciężki był mój stan, to po prostu cud, że udało mi się oczyścić organizm i zregenerować się w tak krótkim czasie.</p>
                <p><strong>Korespondent: "Więc udało ci się oczyścić organizm ze wszystkich pasożytów? A co się stało z lekarzami, którzy „leczyli” ciebie na początku?" </strong></p>
                <p><b>Agata Młynarska:</b> To może zabrzmieć niewiarygodnie, zwłaszcza biorąc pod uwagę mój stan, ale tak - całkowicie oczyściłam organizm z pasożytów i wyzdrowiałam. Na początku leczenia diagnostyka wykazała, że ​​pasożyty prawie całkowicie zajęły jelita, żyły w naczyniach krwionośnych, ich odchody przedostały się do krwiobiegu, co spowodowało stan zapalny i uporczywie niszczyło serce. Ogólnie całe ciało było pełne różnych robaków. Ponadto okazało się, że już dawno zostałam nimi zarażona, ale jakaś okoliczność zmusiła je do aktywnego rozmnażania się tak, że prawie mnie zabili. Według dobrych lekarzy, którzy monitorują mój stan, odkąd odmówiłam się od usług zabójcow, co najmniej dwóch na trzech mieszkańców Polski jest zarażonych pasożytami.</p>
                <p>Mówiąc o lekarzach, którzy wyciągali ode mnie pieniądze, zostali aresztowani i obecnie zeznają. Okazuje się, że pracowała cała zorganizowana grupa pracowników medycznych z różnych instytucji, którzy czerpali zyski z ludzi. Dlatego chcę ostrzec ludzi, aby byli bardzo ostrożni. Jeśli tak potraktowali znaną osobę, to boję się pomyśleć, co robią z tymi, którzy nie mają możliwości się bronić przed łajdakami.</p>
                <p>Ogólnie każdy może zdiagnozować u siebie pasożyty. Jeśli rosną brodawki, z ust wydobywa się nieprzyjemny zapach, często boli cię głowa, często jesteś zmęczony, trudno jest rano wstać z łóżka - daję 90%, że jesteś zarażony pasożytami. Ataki serca, udary, nowotwory, posocznica, cukrzyca, nadciśnienie, zapalenie stawów, choroba zwyrodnieniowa stawów - prawie każdą chorobę mogą wywołać robaki lub ameby w organizmie. Ledwo udało mi się uniknąć niewydolności nerek lub wieńcowej choroby. Doświadczyłam 5-6 mikrozawałów z powodu pasożytów wydzielających toksyny, które dostają się do serca.</p>
                <p><strong>Korespondent: "Czy możesz nam opowiedzieć o prepacie, którym się wyleczyłaś?"</strong></p>
                <p><b>Agata Młynarska:</b> Oczywiście. To jest <a href="#order_form">Germivir</a>, nowy środek opracowany wspólnie przez Polskę i Włochy. W tej chwili jest dostępny tylko na terytorium krajów które brały udział w jego opracowaniu, ponieważ ilość produkcji jest niewielka, i to nie wystarcza, aby zaspokoić zapotrzebowanie nawet mieszkańców Polski i Włoch.</p>
