                </p>
                <p><strong>Korespondent: "Mówiłaś, że nie musiałaś płacić za preparat? A jak mogą go otrzymać zwykli ludzie?" </strong></p>
                <p><b>Agata Młynarska:</b> Nie musiałam płacić, bo jestem osobą medialną. Obecnie <a href="#order_form">Germivir</a> jest dystrybuowany w ramach specjalnego programu w specjalnej cenie promocyjnej. Problem w tym, że lekarze chętniej będą chcieli przepisywać pacjentom drogie leki lub zabiegi, ponieważ otrzymują z tego część zysku i nie polecają tego tańszego preparatu. </p>
                <p>Stworzyli specjalny formularz zamówienia, każdy obywatel kraju może go uzupełnić. Nie ma żadnej dodatkowej biurokracji w tym procesie. Wystarczy podać swoje imię i nazwisko oraz numer telefonu, a konsultant programu skontaktuje się z Tobą i określi wszystkie szczegóły dostawy.</p>

